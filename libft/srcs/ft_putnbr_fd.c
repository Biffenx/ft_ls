#include "libft.h"

void		ft_putnbr_fd(int n, int fd)
{
	long int	nbr;

	nbr = n;
	if (nbr == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = -nbr;
	}
	if (nbr < 10)
	{
		ft_putchar_fd(nbr + '0', fd);
		return ;
	}
	ft_putnbr_fd(nbr / 10, fd);
	ft_putnbr_fd(nbr % 10, fd);
}
